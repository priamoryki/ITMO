#include <bits/stdc++.h>
using namespace std;

int n, m;
vector<long long> a, tree, result;

void mySet(int i, int v) {
    long long x = n + i - 1;
    long long delta = v - tree[x];
    while (x >= 0) {
        tree[x] += delta;
        x = (x + 1) / 2 - 1;
    }
}

long long sum(int l, int r) {
    long long result = 0;
    l += n - 1;
    r += n - 2;
    while (r >= l) {
        if (l % 2 == 0){
            result += tree[l];
        }
        l = l / 2;
        if (r % 2 == 1) {
            result += tree[r];
        }
        r = r / 2 - 1;
    }
    return result;
}

int main() {
    int s = 0;
    cin >> n >> m;
    a.resize(n);
    tree.resize(2 * n - 1);
    result.resize(m);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    for (int i = 0; i < n; i++) {
        tree[n + i - 1] = a[i];
    }
    for (int i = n - 2; i >= 0; i--) {
        tree[i] = tree[2 * i + 1] + tree[2 * i + 2];
    }
    for (int i = 0; i < m; i++) {
        int x, y, z;
        cin >> x >> y >> z;
        if (x == 1) {
            mySet(y, z);
        } else if (x == 2) {
            result[s++] = sum(y, z);
        }
    }
    for (int i = 0; i < s; i++) {
        cout << result[i] << "\n";
    }
}