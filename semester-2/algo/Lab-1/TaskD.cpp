#include <bits/stdc++.h>
using namespace std;

int n, m;
vector<long long> a, result;
vector<long long> tree;

long long nextPowOfTwo(int n) {
    long long result = 1;
    while(result < n) {
        result *= 2;
    }
    return result;
}

void mySet(int i) {
    long long x = n + i - 1;
    long long delta;
    if (tree[x] > 0) {
        delta = -1;
    } else {
        delta = 1;
    }
    while (x >= 0) {
        tree[x] += delta;
        x = (x + 1) / 2 - 1;
    }
}

int index(int id, int x) {
    if (2 * x + 1 < 2 * n - 1) {
        if (id > tree[2 * x + 1]) {
            return index(id - tree[2 * x + 1], 2 * x + 2);
        } else {
            return index(id, 2 * x + 1);
        }
    }
    return x - n + 1;
}

int main() {
    int s = 0;
    cin >> n >> m;
    long long temp = nextPowOfTwo(n);
    a.resize(temp);
    tree.resize(2 * temp - 1);
    result.resize(m);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    a.resize(temp);
    n = temp;
    for (int i = 0; i < n; i++) {
        tree[n + i - 1] = a[i];
    }
    for (int i = n - 2; i >= 0; i--) {
        tree[i] = tree[2 * i + 1] + tree[2 * i + 2];
    }
    for (int i = 0; i < m; i++) {
        int x, y, z;
        cin >> x >> y;
        if (x == 1) {
            mySet(y);
        } else if (x == 2) {
            result[s++] = index(y + 1, 0);
        }
    }
    for (int i = 0; i < s; i++) {
        cout << result[i] << "\n";
    }
}